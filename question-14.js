// 문제 14번 : 자바스크립트에서 사용하는 데이터 타입들을 나열 하세요.

// * 문자열 String

const a = "Hello";
console.log(a)  // hello


// * Number

const b = 7;
console.log(b); // 7


// * Boolean 

const age = 28;
const haha = age >= 28;
console.log(haha);  // true


// * Object 

const obj = { name : 'Seung', age : 28 }
console.log(obj); // { name: 'Seung', age: 28 }


// * Array

const num = [ 1, 2, 3, 4, 5];
console.log(num); // [ 1, 2, 3, 4, 5 ]